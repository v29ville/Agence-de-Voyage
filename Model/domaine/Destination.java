package domaine;

public class Destination {
	private String pays;
	private String continent;
    /**
     * Default constructor
     */
    public Destination() {
    }
    /**
     * Getter/Setter
     */
	public String getPays() {
		return pays;
	}
	public void setPays(String pays) {
		this.pays = pays;
	}
	public String getContinent() {
		return continent;
	}
	public void setContinent(String continent) {
		this.continent = continent;
	}

 
}